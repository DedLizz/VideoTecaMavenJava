package entidad;

public class ReportComentario {

	private int idRerportComent;
	private String comentaReport;
	private int idUserReport;
	
	
	public int getIdRerportComent() {
		return idRerportComent;
	}
	public void setIdRerportComent(int idRerportComent) {
		this.idRerportComent = idRerportComent;
	}
	public String getComentaReport() {
		return comentaReport;
	}
	public void setComentaReport(String comentaReport) {
		this.comentaReport = comentaReport;
	}
	public int getIdUserReport() {
		return idUserReport;
	}
	public void setIdUserReport(int idUserReport) {
		this.idUserReport = idUserReport;
	}
	
	
}
